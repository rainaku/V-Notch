    private static readonly Dictionary<string, IReadOnlyList<string>> TranslationsByText;

    public static string CurrentLanguage => _currentLanguage;

    public static void SetLanguage(string language)
    {
        _currentLanguage = System.Linq.Enumerable.Contains(SupportedLanguages, language) ? language : "en";
    }

    public static CultureInfo GetCulture() => CultureInfo.GetCultureInfo(_currentLanguage switch
    {
        "vi" => "vi-VN",
        "es" => "es-ES",
        "fr" => "fr-FR",
        "de" => "de-DE",
        "ja" => "ja-JP",
        "hi" => "hi-IN",
        _ => "en-US"
    });

    internal static IReadOnlyCollection<string> GetKeys(string language) =>
        _strings.TryGetValue(language, out var strings)
            ? strings.Keys
            : Array.Empty<string>();

